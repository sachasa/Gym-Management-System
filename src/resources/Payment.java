    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        tableDetails();
        int checkid=0;
        String id=jTextFieldPayID.getText();
        String month=jLabelPayDate.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from member where id='"+id+"'");
            while(rs.next())
            {
                checkid=1;
                jTextFieldPayID.setEditable(false);
                jTextFieldPayFirst.setText(rs.getString(2));
                jTextFieldPayLast.setText(rs.getString(3));
                jTextField4PayAddress.setText(rs.getString(5));
                jTextFieldPayPay.setText(rs.getString(9));
            }
            if(checkid==0)
                JOptionPane.showMessageDialog(null, "Member ID does not Exit");
            
            ResultSet rsl=st.executeQuery("select *from payment inner join member where payment.month='"+month+"' and payment.id='"+id+"' and member.id='"+id+"'");
            while(rsl.next())
            {
                jButtonPaySave.setVisible(false);
                JOptionPane.showMessageDialog(null, "Payment is alredy done for this month");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButtonPaySaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPaySaveActionPerformed
        // TODO add your handling code here:
        String id=jTextFieldPayID.getText();
        String month=jLabelPayDate.getText();
        String amount=jTextFieldPayPay.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            PreparedStatement ps=con.prepareStatement("insert into payment values(?,?,?)");
            ps.setString(1, id);
            ps.setString(2, month);
            ps.setString(3, amount);
            ps.executeUpdate();
            tableDetails();
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new Payment().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButtonPaySaveActionPerformed

